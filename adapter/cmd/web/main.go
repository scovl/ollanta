// Package main is the entry point for the ollanta web server.
package main

import (
"context"
"fmt"
"log"
"net/http"
"os/signal"
"syscall"

"github.com/scovl/ollanta/adapter/config"
api "github.com/scovl/ollanta/adapter/primary/http"
"github.com/scovl/ollanta/adapter/secondary/postgres"
"github.com/scovl/ollanta/adapter/secondary/search"
"github.com/scovl/ollanta/adapter/secondary/telemetry"
"github.com/scovl/ollanta/adapter/secondary/webhook"
"github.com/scovl/ollanta/application/ingest"
)

// webhookDispatchAdapter adapts *webhook.Dispatcher to ingest.IWebhookDispatcher.
type webhookDispatchAdapter struct{ d *webhook.Dispatcher }

func (a *webhookDispatchAdapter) Dispatch(ctx context.Context, projectID, scanID int64, event string) error {
return a.d.DispatchScanEvent(ctx, projectID, scanID, event)
}

func main() {
cfg := config.MustLoad()

ctx, stop := signal.NotifyContext(context.Background(), syscall.SIGINT, syscall.SIGTERM)
defer stop()

// ?? Database ??????????????????????????????????????????????????????????
db, err := postgres.New(cfg.DatabaseURL)
if err != nil {
log.Fatalf("postgres: %v", err)
}
defer db.Pool.Close()

projects := postgres.NewProjectRepository(db)
scans := postgres.NewScanRepository(db)
issues := postgres.NewIssueRepository(db)
measures := postgres.NewMeasureRepository(db)
users := postgres.NewUserRepository(db)
groups := postgres.NewGroupRepository(db)
tokens := postgres.NewTokenRepository(db)
sessions := postgres.NewSessionRepository(db)
perms := postgres.NewPermissionRepository(db)
profiles := postgres.NewProfileRepository(db)
gates := postgres.NewGateRepository(db)
periods := postgres.NewNewCodePeriodRepository(db)
webhooksRepo := postgres.NewWebhookRepository(db)

// ?? Search ????????????????????????????????????????????????????????????
searchCfg := search.IndexerConfig{
Host:   cfg.MeilisearchURL,
APIKey: cfg.MeilisearchAPIKey,
}
indexer, err := search.NewMeilisearchIndexer(searchCfg)
if err != nil {
log.Printf("meilisearch indexer unavailable: %v (continuing without search indexing)", err)
indexer = nil
}
searcher, err := search.NewMeilisearchSearcher(searchCfg)
if err != nil {
log.Printf("meilisearch searcher unavailable: %v (continuing without search)", err)
searcher = nil
}

// ?? Telemetry ?????????????????????????????????????????????????????????
metricsReg := telemetry.NewRegistry()

// ?? Webhook dispatcher ????????????????????????????????????????????????
dispatcher := webhook.NewDispatcher(webhooksRepo, 100)
dispatcher.Start(ctx)
defer dispatcher.Stop()

// ?? Search worker ?????????????????????????????????????????????????????
var searchWorker ingest.ISearchEnqueuer
if indexer != nil {
worker := search.NewSearchWorker(indexer, issues, 100)
worker.Start(ctx)
defer worker.Stop()
searchWorker = worker
}

// ?? Application use case ??????????????????????????????????????????????
pipeline := ingest.NewIngestUseCase(projects, scans, issues, measures, searchWorker, &webhookDispatchAdapter{dispatcher})

// ?? HTTP router ???????????????????????????????????????????????????????
handler := api.NewRouter(&api.RouterDeps{
	Cfg: cfg, Projects: projects, Scans: scans, Issues: issues,
	Measures: measures, Users: users, Groups: groups, Tokens: tokens,
	Sessions: sessions, Perms: perms, Searcher: searcher, Indexer: indexer,
	Pipeline: pipeline, Profiles: profiles, Gates: gates,
	Periods: periods, Webhooks: webhooksRepo, Dispatcher: dispatcher,
	Metrics: metricsReg,
})

// ?? HTTP server with graceful shutdown ????????????????????????????????
srv := &http.Server{
Addr:    cfg.Addr,
Handler: handler,
}

go func() {
fmt.Printf("ollanta listening on %s\n", cfg.Addr)
if err := srv.ListenAndServe(); err != nil && err != http.ErrServerClosed {
log.Fatalf("http: %v", err)
}
}()

<-ctx.Done()
log.Println("shutting down...")
shutCtx := context.Background()
_ = srv.Shutdown(shutCtx)
}
